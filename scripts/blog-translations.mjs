// Export helper to get all locales for a post
export function getBlogPostTranslations(postKey) {
  const translations = {}
  for (const [locale, posts] of Object.entries(blogTranslations)) {
    if (posts[postKey]) {
      translations[locale] = posts[postKey]
    }
  }
  return translations
}
